class _MLP(nn.Module):
    def __init__(self, in_dim: int, hidden: int, out_dim: int = 1):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_dim, hidden), nn.LayerNorm(hidden), nn.ReLU(),
            nn.Linear(hidden, hidden), nn.LayerNorm(hidden), nn.ReLU(),
            nn.Linear(hidden, out_dim),
        )

    def forward(self, x):
        return self.net(x)


class _VNet(nn.Module):
    def __init__(self, sdim: int, hidden: int):
        super().__init__()
        self.net = _MLP(sdim, hidden)

    def forward(self, s):
        return self.net(s).squeeze(-1)


class _QNet(nn.Module):
    def __init__(self, sdim: int, adim: int, hidden: int):
        super().__init__()
        self.net = _MLP(sdim + adim, hidden)

    def forward(self, s, a):
        return self.net(torch.cat([s, a], dim=-1)).squeeze(-1)


class _ReplayBuffer:
    def __init__(self, transitions: List[dict], device: torch.device):
        n    = len(transitions)
        sdim = transitions[0]["s"].shape[0]
        adim = transitions[0]["a"].shape[0]
        self.s      = torch.zeros(n, sdim, device=device)
        self.a      = torch.zeros(n, adim, device=device)
        self.r      = torch.zeros(n, device=device)
        self.s_next = torch.zeros(n, sdim, device=device)
        self.done   = torch.zeros(n, device=device)
        for i, t in enumerate(transitions):
            self.s[i]      = torch.from_numpy(t["s"])
            self.a[i]      = torch.from_numpy(t["a"])
            self.r[i]      = t["r"]
            self.s_next[i] = torch.from_numpy(t["s_next"])
            self.done[i]   = t["done"]
        self.n = n; self.device = device
        # normalise rewards
        mu = self.r.mean(); sg = self.r.std().clamp(min=1e-6)
        self.r = (self.r - mu) / sg

    def sample(self, bs: int):
        idx = torch.randint(0, self.n, (bs,), device=self.device)
        return self.s[idx], self.a[idx], self.r[idx], self.s_next[idx], self.done[idx]


def _expectile_loss(diff: torch.Tensor, tau: float) -> torch.Tensor:
    """Lτ²(u) = |τ − 𝟙(u<0)| · u²"""
    w = torch.where(diff >= 0, torch.full_like(diff, tau),
                    torch.full_like(diff, 1.0 - tau))
    return (w * diff.pow(2)).mean()


class _IQLAgent:
    """
    Implicit Q-Learning (Kostrikov et al., ICLR 2022).

    L_V = E[Lτ²(Q(s,a) − V(s))]           (expectile regression)
    L_Q = E[(r + γ·V(s') − Q(s,a))²]       (TD error)
    π   = argmax_a Q(s, a)                  (greedy extraction)
    """

    def __init__(self, sdim: int, adim: int, hidden: int = 256,
                 gamma: float = 0.99, tau: float = 0.7, lr: float = 3e-4,
                 device: Optional[torch.device] = None):
        self.gamma = gamma; self.tau = tau
        self.device = device or torch.device(
            "cuda" if torch.cuda.is_available() else "cpu")

        self.v_net   = _VNet(sdim, hidden).to(self.device)
        self.q_net   = _QNet(sdim, adim, hidden).to(self.device)
        self.q_tgt   = _QNet(sdim, adim, hidden).to(self.device)
        self.q_tgt.load_state_dict(self.q_net.state_dict())
        for p in self.q_tgt.parameters():
            p.requires_grad = False

        self.v_opt = optim.Adam(self.v_net.parameters(), lr=lr)
        self.q_opt = optim.Adam(self.q_net.parameters(), lr=lr)

    def _step(self, s, a, r, sn, done, polyak=0.005):
        with torch.no_grad():
            q_tgt_val = r + self.gamma * (1 - done) * self.v_net(sn)
        q_pred = self.q_net(s, a)
        q_loss = nn.functional.mse_loss(q_pred, q_tgt_val)
        self.q_opt.zero_grad(); q_loss.backward()
        nn.utils.clip_grad_norm_(self.q_net.parameters(), 1.0)
        self.q_opt.step()

        with torch.no_grad():
            q_val = self.q_net(s, a)
        v_pred = self.v_net(s)
        v_loss = _expectile_loss(q_val - v_pred, self.tau)
        self.v_opt.zero_grad(); v_loss.backward()
        nn.utils.clip_grad_norm_(self.v_net.parameters(), 1.0)
        self.v_opt.step()

        for p, tp in zip(self.q_net.parameters(), self.q_tgt.parameters()):
            tp.data.mul_(1 - polyak).add_(p.data, alpha=polyak)

        return q_loss.item(), v_loss.item(), q_pred.mean().item()

    def train(self, buf: _ReplayBuffer, n_steps: int, batch_size: int,
              log_interval: int = 5000, verbose: bool = True):
        rq = rv = rqm = 0.0
        for step in range(1, n_steps + 1):
            ql, vl, qm = self._step(*buf.sample(batch_size))
            rq += ql; rv += vl; rqm += qm
            if step % log_interval == 0 and verbose:
                print(f"[IQL {step:>6}] Q_loss={rq/log_interval:.4f}  "
                      f"V_loss={rv/log_interval:.4f}  Q_mean={rqm/log_interval:.3f}")
                rq = rv = rqm = 0.0

    @torch.no_grad()
    def score(self, s_vec: np.ndarray, a_vecs: List[np.ndarray]) -> np.ndarray:
        s = torch.from_numpy(s_vec).float().to(self.device)
        A = torch.from_numpy(np.stack(a_vecs)).float().to(self.device)
        S = s.unsqueeze(0).expand(len(a_vecs), -1)
        return self.q_net(S, A).cpu().numpy()


