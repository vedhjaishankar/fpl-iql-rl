N_ROLLOUTS    = 150
EPSILON       = 0.30
MAX_CAND      = 30
IQL_STEPS     = 25_000
BATCH_SIZE    = 256
GAMMA         = 0.99
TAU_EXPECTILE = 0.7
LR            = 3e-4
HIDDEN        = 256
LOG_INTERVAL  = 5000


class StudentPolicy:
    """
    IQL-based FPL policy.

    fit(train_data):
        Trains a HistGBM reward model then an IQL agent from offline
        epsilon-greedy rollouts on the provided historical data.

    reset():
        Resets per-rollout budget tracking (call before each new episode).

    act(state):
        state = (gameweek: int, squad: tuple[int, ...])
        Returns a list of at most one transfer [(player_out_id, player_in_id)]
        or [] for no transfer.  Respects budget, positional composition,
        and the 3-players-per-club rule.
    """

    def __init__(self):
        self._predictor:  Optional[_PointsPredictor] = None
        self._iql:        Optional[_IQLAgent]         = None
        self._cache:      Optional[FeatureCache]       = None
        self._price_dict: Dict[Tuple[int, int], float] = {}
        self._by_pos:     Dict[str, List[int]]         = {
            "GK": [], "DEF": [], "MID": [], "FWD": []
        }
        self._max_gw: int   = 38
        self._budget: float = 0.0
        self._last_squad: Optional[tuple] = None
        self._init: bool = False

    # -----------------------------------------------------------------------
    def fit(self, train_data: pd.DataFrame) -> None:
        """Train reward model and IQL agent on historical FPL data."""
        print("[StudentPolicy] Building features...")
        feat_df = _build_features(train_data)
        self._max_gw = int(feat_df["GW"].max())

        print("[StudentPolicy] Building feature cache...")
        self._cache = _build_feature_cache(feat_df)
        for _, row in feat_df.iterrows():
            self._price_dict[(int(row["element"]), int(row["GW"]))] = float(
                row["price"])
        for pos in ["GK", "DEF", "MID", "FWD"]:
            self._by_pos[pos] = [
                pid for pid, p in self._cache["position"].items() if p == pos
            ]

        print("[StudentPolicy] Training reward model...")
        self._predictor = _PointsPredictor()
        stats = self._predictor.fit(feat_df, verbose=True)
        print(f"[StudentPolicy] CV MAE={stats['cv_mae']:.4f}")

        print(f"[StudentPolicy] Collecting offline data ({N_ROLLOUTS} rollouts)...")
        env = _FPLEnv(feat_df, self._predictor, self._cache,
                      noise_std=1.0, seed=0)
        transitions = _collect_offline_data(
            env, n_rollouts=N_ROLLOUTS, epsilon=EPSILON,
            max_cand=MAX_CAND, seed=42,
        )
        print(f"[StudentPolicy] Collected {len(transitions)} transitions.")

        device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        print(f"[StudentPolicy] Training IQL on {device} for {IQL_STEPS} steps...")
        buf = _ReplayBuffer(transitions, device)
        self._iql = _IQLAgent(STATE_DIM, ACTION_DIM, HIDDEN, GAMMA,
                               TAU_EXPECTILE, LR, device)
        self._iql.train(buf, IQL_STEPS, BATCH_SIZE, LOG_INTERVAL, verbose=True)
        print("[StudentPolicy] fit() complete.")

    # -----------------------------------------------------------------------
    def reset(self) -> None:
        self._budget     = 0.0
        self._last_squad = None
        self._init       = False

    # -----------------------------------------------------------------------
    def act(self, state) -> List[Tuple[int, int]]:
        if self._cache is None or self._iql is None:
            return []

        gw, squad_tuple = state
        gw    = int(gw)
        squad = list(squad_tuple)

        if not self._init:
            cost = sum(self._price(p, gw) for p in squad)
            self._budget = max(0.0, _TOTAL_BUDGET - cost)
            self._init   = True
        elif self._last_squad is not None and set(squad_tuple) != set(self._last_squad):
            cost = sum(self._price(p, gw) for p in squad)
            self._budget = max(0.0, _TOTAL_BUDGET - cost)
        self._last_squad = squad_tuple

        actions = self._valid_actions(squad, gw)
        if len(actions) <= 1:
            return []

        s_vec = _build_state_vec(squad, gw, self._budget, self._cache,
                                 self._max_gw)
        a_vecs = [
            (_build_action_vec(0, 0, gw, self._cache, self._budget,
                               no_transfer=True)
             if a is None else
             _build_action_vec(a[0], a[1], gw, self._cache, self._budget))
            for a in actions
        ]
        q_scores = self._iql.score(s_vec, a_vecs)

        no_tr_score = q_scores[next(i for i, a in enumerate(actions) if a is None)]
        best_idx    = int(np.argmax(q_scores))
        best        = actions[best_idx]

        if best is None or q_scores[best_idx] <= no_tr_score + 0.01:
            return []

        p_out, p_in = best
        self._budget += self._price(p_out, gw) - self._price(p_in, gw)
        return [best]

    # -----------------------------------------------------------------------
    def predict_reward(self, player_dict: dict) -> float:
        """Predict reward for a player given a feature dict (evaluation helper)."""
        if self._predictor is None:
            return 2.0
        feats = np.array(
            [float(player_dict.get(c, 0.0) or 0.0) for c in FEATURE_COLS],
            dtype=np.float32,
        )
        return self._predictor.predict_one(feats)

    # -----------------------------------------------------------------------
    # Internal helpers
    # -----------------------------------------------------------------------

    def _price(self, pid: int, gw: int) -> float:
        k = (pid, gw)
        if k in self._price_dict:
            return self._price_dict[k]
        for off in range(1, 10):
            for d in (-off, off):
                k2 = (pid, gw + d)
                if k2 in self._price_dict:
                    return self._price_dict[k2]
        return 5.0

    def _valid_actions(self, squad: List[int], gw: int) -> List:
        actions   = [None]
        squad_set = set(squad)
        pm        = _pos_map(self._cache, squad)
        tc        = _count_teams(squad, self._cache["team"])
        for pos, out_players in pm.items():
            cands = self._candidates(pos, squad_set, gw)
            for p_out in out_players:
                po      = self._price(p_out, gw)
                team_out = self._cache["team"].get(p_out, "")
                for p_in in cands:
                    pi = self._price(p_in, gw)
                    if pi > po + self._budget + 0.01:
                        continue
                    team_in = self._cache["team"].get(p_in, "")
                    adj = (tc.get(team_in, 0)
                           + (-1 if team_in == team_out else 0) + 1)
                    if adj > _MAX_PER_CLUB:
                        continue
                    actions.append((p_out, p_in))
        return actions

    def _candidates(self, pos: str, squad_set: set, gw: int,
                    max_n: int = 30) -> List[int]:
        all_pos = [p for p in self._by_pos.get(pos, []) if p not in squad_set]
        scored  = [(_get_feats(self._cache, p, gw)[1]
                    + _get_feats(self._cache, p, gw)[8], p)
                   for p in all_pos]
        scored.sort(reverse=True)
        return [p for _, p in scored[:max_n]]
