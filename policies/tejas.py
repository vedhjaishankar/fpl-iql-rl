class _PointsPredictor:
    """
    HistGradientBoostingRegressor wrapper predicting total_points
    from FEATURE_COLS.  Cross-validates for MAE reporting, then trains
    a final model on all data.
    """

    def __init__(self):
        self.model: Optional[HistGradientBoostingRegressor] = None
        self._mean: float = 2.0

    def fit(self, feature_df: pd.DataFrame, verbose: bool = False) -> dict:
        df = feature_df.dropna(subset=["total_points"])
        X = df[FEATURE_COLS].fillna(0.0).astype(np.float32).values
        y = df["total_points"].values.astype(np.float32)
        self._mean = float(np.mean(y))

        cv_params = dict(max_iter=400, learning_rate=0.05, max_leaf_nodes=63,
                         max_depth=6, min_samples_leaf=20, l2_regularization=0.1,
                         random_state=42, early_stopping=True,
                         validation_fraction=0.1, n_iter_no_change=30, verbose=0)

        kf = KFold(n_splits=5, shuffle=True, random_state=42)
        oof = np.zeros(len(y))
        for _, (tr, va) in enumerate(kf.split(X)):
            m = HistGradientBoostingRegressor(**cv_params)
            m.fit(X[tr], y[tr])
            oof[va] = m.predict(X[va])
        cv_mae = mean_absolute_error(y, oof)

        self.model = HistGradientBoostingRegressor(
            max_iter=400, learning_rate=0.05, max_leaf_nodes=63,
            max_depth=6, min_samples_leaf=20, l2_regularization=0.1,
            random_state=42, early_stopping=False,
        )
        self.model.fit(X, y)

        if verbose:
            print(f"[PointsPredictor] CV MAE={cv_mae:.4f}  mean_pts={self._mean:.2f}")
        return {"cv_mae": cv_mae, "mean_points": self._mean}

    def predict_batch(self, X: np.ndarray) -> np.ndarray:
        if self.model is None:
            return np.full(len(X), self._mean)
        return np.maximum(0.0, self.model.predict(X.astype(np.float32)))

    def predict_one(self, feats: np.ndarray) -> float:
        return float(self.predict_batch(feats.reshape(1, -1))[0])


