"""
FPL IQL Policy — single self-contained file
============================================
Implements the required StudentPolicy interface:
    fit(train_data)  - train reward model + IQL on historical data
    reset()          -  reset per-rollout state
    act(state)       -  return list of transfers [(player_out, player_in), ...]

Strategy: Implicit Q-Learning (Kostrikov et al., ICLR 2022)
  1. HistGradientBoosting reward model predicts player points from
     14 hand-crafted rolling features per player per gameweek.
  2. An offline replay buffer is generated by running epsilon-greedy
     rollouts on the training data using the reward model as simulator.
  3. IQL (V-net + Q-net with expectile value loss) is trained on the buffer.
  4. At inference, valid transfers are scored by the Q-network and the best
     one is returned (with a no-transfer baseline).

Dependencies: numpy, pandas, torch, scikit-learn  (all standard / pip-installable)
"""

import numpy as np
import pandas as pd
from typing import Dict, List, Optional, Tuple, Union

import torch
import torch.nn as nn
import torch.optim as optim

from sklearn.ensemble import HistGradientBoostingRegressor
from sklearn.model_selection import KFold
from sklearn.metrics import mean_absolute_error


# ===========================================================================
# ── SECTION 1: Feature Engineering ─────────────────────────────────────────
# ===========================================================================

POSITION_ENC = {"GK": 0, "DEF": 1, "MID": 2, "FWD": 3}

FEATURE_COLS = [
    "price",            # 0  – player price in £M
    "form_3gw",         # 1  – rolling avg total_points last 3 GWs
    "form_5gw",         # 2  – rolling avg total_points last 5 GWs
    "minutes_form_3gw", # 3  – rolling avg minutes last 3 GWs
    "goals_3gw",        # 4  – goals scored last 3 GWs
    "assists_3gw",      # 5  – assists last 3 GWs
    "clean_sheets_3gw", # 6  – clean sheets last 3 GWs
    "starts_pct_3gw",   # 7  – fraction of last 3 GWs started (>45 min)
    "xP",               # 8  – expected points (provided in data)
    "was_home",         # 9  – home fixture flag
    "opp_strength",     # 10 – normalised opponent difficulty [0,1]
    "position_enc",     # 11 – ordinal position {GK:0, DEF:1, MID:2, FWD:3}
    "bonus_3gw",        # 12 – bonus points last 3 GWs
    "bps_3gw",          # 13 – avg BPS last 3 GWs
]

N_FEATURES = len(FEATURE_COLS)   # 14
STATE_DIM  = 2 + 15 * N_FEATURES # 212
ACTION_DIM = 11

FeatureCache = Dict  # type alias


def _build_features(df: pd.DataFrame) -> pd.DataFrame:
    """Enrich raw merged_gw DataFrame with engineered FEATURE_COLS."""
    df = df.copy()
    if "GW" not in df.columns:
        df["GW"] = df["round"] if "round" in df.columns else 1

    for col in ["value", "total_points", "minutes", "goals_scored", "assists",
                "clean_sheets", "saves", "bonus", "bps", "goals_conceded"]:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0)

    df["xP"]          = pd.to_numeric(df.get("xP", 0), errors="coerce").fillna(0)
    df["was_home"]    = df["was_home"].astype(int) if "was_home" in df.columns else 0
    df["price"]       = df["value"] / 10.0
    df["position_enc"] = df["position"].map(POSITION_ENC).fillna(0).astype(int)

    df = df.sort_values(["element", "GW"]).reset_index(drop=True)
    grp = df.groupby("element", sort=False)

    def roll_mean(col, n):
        return grp[col].transform(
            lambda x: x.shift(1).rolling(n, min_periods=1).mean()
        ).fillna(0)

    def roll_sum(col, n):
        return grp[col].transform(
            lambda x: x.shift(1).rolling(n, min_periods=1).sum()
        ).fillna(0)

    df["form_3gw"]         = roll_mean("total_points", 3)
    df["form_5gw"]         = roll_mean("total_points", 5)
    df["minutes_form_3gw"] = roll_mean("minutes", 3)
    df["goals_3gw"]        = roll_sum("goals_scored", 3)
    df["assists_3gw"]      = roll_sum("assists", 3)
    df["clean_sheets_3gw"] = roll_sum("clean_sheets", 3)
    df["bonus_3gw"]        = roll_sum("bonus", 3)
    df["bps_3gw"]          = roll_mean("bps", 3)
    df["started"]          = (df["minutes"] > 45).astype(float)
    df["starts_pct_3gw"]   = roll_mean("started", 3)

    if "opponent_team" in df.columns and "goals_conceded" in df.columns:
        opp_raw = df.groupby("opponent_team")["goals_conceded"].mean()
        mn, mx  = opp_raw.min(), opp_raw.max()
        rng     = mx - mn if mx != mn else 1.0
        df["opp_strength"] = df["opponent_team"].map(
            ((opp_raw - mn) / rng).to_dict()
        ).fillna(0.5)
    else:
        df["opp_strength"] = 0.5

    return df


def _build_feature_cache(feature_df: pd.DataFrame) -> FeatureCache:
    """Build O(1) lookup: {(element, gw) -> feature_vector}."""
    exact: Dict[Tuple[int, int], np.ndarray] = {}
    latest: Dict[int, np.ndarray] = {}
    position: Dict[int, str] = {}
    team: Dict[int, str] = {}
    zero = np.zeros(N_FEATURES, dtype=np.float32)

    for _, row in feature_df.sort_values(["element", "GW"]).iterrows():
        eid = int(row["element"])
        gw  = int(row["GW"])
        feats = np.array(
            [float(row.get(c, 0.0) or 0.0) for c in FEATURE_COLS],
            dtype=np.float32,
        )
        exact[(eid, gw)] = feats
        latest[eid] = feats
        if eid not in position:
            position[eid] = str(row.get("position", "MID"))
            team[eid]     = str(row.get("team", ""))

    return {"exact": exact, "latest": latest, "position": position,
            "team": team, "zero": zero}


def _get_feats(cache: FeatureCache, pid: int, gw: int) -> np.ndarray:
    """O(1) feature lookup with nearest-earlier-GW fallback."""
    key = (pid, gw)
    if key in cache["exact"]:
        return cache["exact"][key]
    for g in range(gw - 1, 0, -1):
        k = (pid, g)
        if k in cache["exact"]:
            return cache["exact"][k]
    return cache["latest"].get(pid, cache["zero"]).copy()


def _pos_map(cache: FeatureCache, squad: List[int]) -> Dict[str, List[int]]:
    pm: Dict[str, List[int]] = {"GK": [], "DEF": [], "MID": [], "FWD": []}
    for pid in squad:
        p = cache["position"].get(pid, "MID")
        pm[p if p in pm else "MID"].append(pid)
    return pm


def _count_teams(squad: List[int], player_team: Dict[int, str]) -> Dict[str, int]:
    counts: Dict[str, int] = {}
    for pid in squad:
        t = player_team.get(pid, "")
        counts[t] = counts.get(t, 0) + 1
    return counts


def _build_state_vec(squad: List[int], gw: int, budget: float,
                     cache: FeatureCache, max_gw: int = 38) -> np.ndarray:
    """212-dim state: [gw_norm, budget_norm, 15×14 player features]."""
    pf  = {pid: _get_feats(cache, pid, gw) for pid in squad}
    pm  = _pos_map(cache, squad)
    gks  = sorted(pm["GK"],  key=lambda p: -pf[p][1])
    defs = sorted(pm["DEF"], key=lambda p: -pf[p][1])
    mids = sorted(pm["MID"], key=lambda p: -pf[p][1])
    fwds = sorted(pm["FWD"], key=lambda p: -pf[p][1])
    z = np.zeros(N_FEATURES, dtype=np.float32)
    slots = [("GK", 2, gks), ("DEF", 5, defs), ("MID", 5, mids), ("FWD", 3, fwds)]
    parts = [np.array([gw / max_gw, np.clip(budget / 100.0, 0, 1)], dtype=np.float32)]
    for _, n, players in slots:
        for i in range(n):
            parts.append(pf[players[i]] if i < len(players) else z)
    return np.concatenate(parts)


def _build_action_vec(p_out: int, p_in: int, gw: int, cache: FeatureCache,
                      budget: float, no_transfer: bool = False) -> np.ndarray:
    """11-dim action vector."""
    if no_transfer:
        return np.zeros(ACTION_DIM, dtype=np.float32)
    fo = _get_feats(cache, p_out, gw)
    fi = _get_feats(cache, p_in,  gw)
    dp = fi[1] - fo[1]
    dv = fo[0] - fi[0]
    return np.array([
        1.0,
        fo[11] / 3.0, fi[11] / 3.0,
        fo[1]  / 15.0, fo[0] / 15.0,
        fi[1]  / 15.0, fi[0] / 15.0,
        dp     / 15.0,
        dv     / 10.0,
        np.clip((budget + dv) / 100.0, -1.0, 1.0),
        float(fo[11] == fi[11]),
    ], dtype=np.float32)


