_POSITION_COUNTS = {"GK": 2, "DEF": 5, "MID": 5, "FWD": 3}
_TOTAL_BUDGET    = 100.0
_MAX_PER_CLUB    = 3
_TRANSFER_PENALTY = 4.0


class _FPLEnv:
    """Full FPL simulation environment (15-player squad, budget, club limits)."""

    def __init__(self, feature_df: pd.DataFrame, predictor: _PointsPredictor,
                 cache: FeatureCache, noise_std: float = 1.0, seed: int = 42):
        self.feature_df = feature_df
        self.predictor  = predictor
        self.cache      = cache
        self.noise_std  = noise_std
        self.rng        = np.random.default_rng(seed)

        self.all_gws = sorted(feature_df["GW"].unique())
        self.min_gw  = self.all_gws[0]
        self.max_gw  = self.all_gws[-1]

        self.player_position = cache["position"]
        self.player_team     = cache["team"]

        self.players_by_pos: Dict[str, List[int]] = {
            "GK": [], "DEF": [], "MID": [], "FWD": []
        }
        for pid, pos in self.player_position.items():
            if pos in self.players_by_pos:
                self.players_by_pos[pos].append(pid)

        self._cand_cache: Dict[Tuple[str, int], List[int]] = {}
        self._price: Dict[Tuple[int, int], float] = {}
        for _, row in feature_df.iterrows():
            self._price[(int(row["element"]), int(row["GW"]))] = float(row["price"])

        self.squad: List[int] = []
        self.budget: float    = 0.0
        self.gw: int          = self.min_gw

    def get_price(self, pid: int, gw: int) -> float:
        k = (pid, gw)
        if k in self._price:
            return self._price[k]
        for off in range(1, 10):
            for d in (-off, off):
                k2 = (pid, gw + d)
                if k2 in self._price:
                    return self._price[k2]
        return 5.0

    def reset(self, squad: Optional[List[int]] = None) -> Tuple:
        self.gw = self.min_gw
        self.squad = list(squad) if squad else self._random_squad(self.gw)
        self.budget = max(0.0, _TOTAL_BUDGET - sum(
            self.get_price(p, self.gw) for p in self.squad
        ))
        return (self.gw, tuple(self.squad))

    def step(self, action) -> Tuple[Tuple, float, bool]:
        transfers = ([action] if isinstance(action, tuple) else
                     action   if isinstance(action, list) else [])
        penalty = 0.0
        for p_out, p_in in transfers:
            if p_out not in self.squad:
                continue
            po = self.get_price(p_out, self.gw)
            pi = self.get_price(p_in,  self.gw)
            if pi > po + self.budget + 0.01:
                continue
            self.squad = [p_in if p == p_out else p for p in self.squad]
            self.budget += po - pi
            penalty += _TRANSFER_PENALTY  # always 1 free transfer used
        # correct: first transfer is free, only additional ones cost -4
        # re-compute: if transfers ≤ 1, no penalty
        if len(transfers) <= 1:
            penalty = 0.0
        else:
            penalty = (len(transfers) - 1) * _TRANSFER_PENALTY

        X = np.array([_get_feats(self.cache, p, self.gw) for p in self.squad],
                     dtype=np.float32)
        preds = self.predictor.predict_batch(X)
        reward = float(preds.sum())
        if self.noise_std > 0:
            reward += np.random.normal(0, self.noise_std * np.sqrt(len(self.squad)))
        reward = max(0.0, reward) - penalty

        self.gw += 1
        done = self.gw > self.max_gw
        return (self.gw, tuple(self.squad)), reward, done

    def get_valid_actions(self, squad: List[int], gw: int,
                          budget: float, max_cand: int = 30) -> List:
        actions   = [None]
        squad_set = set(squad)
        pm        = _pos_map(self.cache, squad)
        tc        = _count_teams(squad, self.player_team)
        for pos, out_players in pm.items():
            cands = self._candidates(pos, squad_set, gw, max_cand)
            for p_out in out_players:
                po      = self.get_price(p_out, gw)
                team_out = self.player_team.get(p_out, "")
                for p_in in cands:
                    pi = self.get_price(p_in, gw)
                    if pi > po + budget + 0.01:
                        continue
                    team_in = self.player_team.get(p_in, "")
                    adj = (tc.get(team_in, 0)
                           + (-1 if team_in == team_out else 0) + 1)
                    if adj > _MAX_PER_CLUB:
                        continue
                    actions.append((p_out, p_in))
        return actions

    def _candidates(self, pos: str, squad_set: set, gw: int, max_n: int) -> List[int]:
        key = (pos, gw)
        if key not in self._cand_cache:
            scored = [(  _get_feats(self.cache, pid, gw)[1], pid)
                      for pid in self.players_by_pos.get(pos, [])]
            scored.sort(reverse=True)
            self._cand_cache[key] = [p for _, p in scored]
        return [p for p in self._cand_cache[key] if p not in squad_set][:max_n]

    def _random_squad(self, gw: int) -> List[int]:
        budget = _TOTAL_BUDGET
        squad: List[int] = []
        tc: Dict[str, int] = {}
        for pos, n in _POSITION_COUNTS.items():
            avail = self.players_by_pos.get(pos, []).copy()
            self.rng.shuffle(avail)
            added = 0
            for pid in avail:
                if added >= n:
                    break
                if pid in squad:
                    continue
                pr  = self.get_price(pid, gw)
                tm  = self.player_team.get(pid, "")
                if tc.get(tm, 0) >= _MAX_PER_CLUB or pr > budget:
                    continue
                squad.append(pid); budget -= pr
                tc[tm] = tc.get(tm, 0) + 1; added += 1
            for pid in avail:
                if added >= n:
                    break
                if pid not in squad:
                    squad.append(pid)
                    tc[self.player_team.get(pid, "")] = (
                        tc.get(self.player_team.get(pid, ""), 0) + 1)
                    added += 1
        return squad[:15]


def _collect_offline_data(env: _FPLEnv, n_rollouts: int, epsilon: float,
                          max_cand: int, seed: int) -> List[dict]:
    """Epsilon-greedy rollouts → list of (s, a, r, s', done) dicts."""
    rng  = np.random.default_rng(seed)
    data = []
    for _ in range(n_rollouts):
        state = env.reset()
        done  = False
        while not done:
            gw, squad = state
            budget = env.budget
            actions = env.get_valid_actions(list(squad), gw, budget, max_cand)
            if rng.random() < epsilon or len(actions) == 1:
                action = actions[int(rng.integers(len(actions)))]
            else:
                action = _greedy(actions, gw, env.cache)
            s_vec = _build_state_vec(list(squad), gw, budget, env.cache,
                                     env.max_gw)
            a_vec = (_build_action_vec(0, 0, gw, env.cache, budget,
                                       no_transfer=True)
                     if action is None else
                     _build_action_vec(action[0], action[1], gw, env.cache,
                                       budget))
            next_state, reward, done = env.step(action)
            ng, ns = next_state
            sn_vec = _build_state_vec(list(ns), ng, env.budget, env.cache,
                                      env.max_gw)
            data.append({"s": s_vec, "a": a_vec, "r": float(reward),
                         "s_next": sn_vec, "done": float(done)})
            state = next_state
    return data


def _greedy(actions, gw: int, cache: FeatureCache):
    best, score = None, -np.inf
    for a in actions:
        s = (0.0 if a is None else
             (_get_feats(cache, a[1], gw)[1] + _get_feats(cache, a[1], gw)[8])
             - (_get_feats(cache, a[0], gw)[1] + _get_feats(cache, a[0], gw)[8]))
        if s > score:
            score, best = s, a
    return best


